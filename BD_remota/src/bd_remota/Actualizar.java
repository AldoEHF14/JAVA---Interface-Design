    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        String id = txt_id.getText();
        String act = txt_update.getText();

        if (id.isEmpty() || act.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }
        
        try {
            //String driver = "com.mysql.jdbc.Driver";
            String url = "jdbc:mysql://localhost:3306/alumnosbd";
            String usuario = "AldoE";
            String pass = "2193035908";
            //Class.forName(driver);
            Connection cn = DriverManager.getConnection(url, usuario, pass);
            //String act = txt_update.getText();
            //String id = txt_id.getText();
            String query ="UPDATE alumnos SET Nombre =' "+act+" ' WHERE ID='"+id+"'";
            
            Statement stmt = (Statement) cn.createStatement();
            stmt.executeUpdate(query);
            
            
            JOptionPane.showMessageDialog(null, "Actualizado Correctamente");
            
            this.txt_id.setText("");
            this.txt_update.setText("");


        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

